namespace PNGNormalizer
{
    /// <summary>
    /// Represents the byte order.
    /// </summary>
    public enum Endianness
    {
        /// <summary>
        /// Little-endian. Least significant byte first.
        /// </summary>
        LittleEndian,
        
        /// <summary>
        /// Big-endian. Most significant byte first.
        /// </summary>
        BigEndian
    }
}